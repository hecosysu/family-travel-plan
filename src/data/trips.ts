export const getTripsByYear = () => {
  const grouped: Record<number, Trip[]> = {};
  tripsData.forEach(trip => {
    if (!grouped[trip.year]) {
      grouped[trip.year] = [];
    }
    grouped[trip.year].push(trip);
  });
  return grouped;
};

export const getTripById = (id: string) => {
  return tripsData.find(trip => trip.id === id);
};

export const getTripsByPhase = () => {
  const phase1 = tripsData.filter(trip => trip.year >= 2026 && trip.year <= 2040);
  const phase2 = tripsData.filter(trip => trip.year >= 2041 && trip.year <= 2055);
  return { phase1, phase2 };
};
